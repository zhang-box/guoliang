  <div class="content-wrapper">
    <div class="content-title">
      <el-button class="title-button" size="mini" @click="returnList"><i class="el-icon-arrow-left"></i> 返回</el-button>
        <span class="title-info">生成报告</span>
    </div>
    <div class="report-header">
      <div class="report-date">
        <span class="turn-right-margin">报告期</span>
        <el-select v-model="reportNum" placeholder="请选择" style="width:200px;" disabled>
          <el-option v-model="options3"></el-option>
        </el-select>
      </div>
      <div class="report-btn">
        <el-button type="primary" icon="el-icon-upload" class="search-btn" @click="exportReport">导出</el-button>
        <el-button type="primary" icon="el-icon-printer" class="search-btn" @click="doPrint">打印</el-button>
      </div>
      <div id="print">
      <div class="report-title" ref="title">国家粮食和物资储备局<span>{{year}}{{month}}月份</span>督办情况统计</div>
      <div>
        <div class="report-text-tlt">一、月度报告概述</div>
        <div style="width:100%;border:1px solid #ccc;height:100px;padding: 10px 20px;border-radius: 5px;">{{notes}}</div>
        <!--<el-input type="textarea" :autosize="{ minRows: 2, maxRows: 4}" v-model="notes" class="report-textarea" disabled>-->
        <!--</el-input>-->
      </div>

    <div class="list">
      <div class="report-table-tit">二、月度报告统计</div>
      <el-table :data="tableData" class="table1" stripe="" style="width: 100%">
        <el-table-column prop="takeUnit" label="承办单位"></el-table-column>
        <el-table-column prop="title" label="事件标题" width="240"></el-table-column>
        <el-table-column prop="createTime" label="登记时间"></el-table-column>
        <el-table-column prop="processStatus" label="处理情况"></el-table-column>
        <el-table-column prop="eventType" label="事件类型"></el-table-column>
      </el-table>
    </div>
  </div>
  <div class="report-last-btn">
      <el-button type="primary" style="margin-left: 30px;" @click="returnList">返回</el-button>
  </div>
</div>
</div>
</template>
<script>
  import { getReportById, exportReport} from  'api/supervision/index'
  import { Message } from 'element-ui'
  import {worksheetUrl} from 'api/index'
  export default {
    data() {
      return {
        notes:'',
        year:'',
        month:'',
        reportNum:'',
        input:'',
        input1:'',
        input2:'',
        input3:'',
        value2:'',
        value3:'',
        textarea3:'',
        checked:true,
        checked2:"",
        checked3:"",
        checked4:"",
        dialogVisible: false,
        showType: false,
        options2: [],
        options3: '',
        tableData: [],
        formPage: {
          page: 1,
          limit: 10,
          total: undefined,
        },
        activityName:null,
        time:null
      }
    },
    created(){
      let data = {
        "reportId":this.$route.params.reportId
      }
      getReportById(data).then(response => {
        this.tableData = response.data.wookSheets
        this.reportNum = response.data.reportNumber
        this.notes = response.data.notes
        this.options3 = response.data.reportNumber
        this.year = this.options3.substr(0,5)
        this.month = this.options3.substr(6,1)
      })

      },
    methods:{
        doPrint() {
        	let subPrint = document.getElementById('print');
            console.log(subPrint);
            let newContent = subPrint.innerHTML;
        	let oldContent = newContent.innerHTML;
        	document.body.innerHTML = newContent;
        	window.print();
            window.location.reload() //解决打印取消  返回页面鼠标失效,不能继续打印
        	document.body.innerHTML = oldContent;
            return false;
        },
      returnList(){
        this.$router.push({name:'overseeReport'});  /* 点击返回跳转 */
      },
      handleClose(done) {
        this.$confirm('确认关闭？')
          .then(_ => {
            done();
          })
          .catch(_ => {});
      },
      print(){
        window.print()
      },
      exportReport: function () {
        let data = {
          'reportId': this.$route.params.reportId,
          'rowRD': this.reportNum,
          'rowTitle': this.$refs.title.innerText,
          'type': 3,
          'monthlyReport': '',
        }
        exportReport(data).then(response => {
          window.location.href = worksheetUrl + response.data
        })
      }
    }
  }
</script>
